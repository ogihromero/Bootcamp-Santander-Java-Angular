// tipos de funções

//funções declarativas
function funcaoDeclarativa() {
  console.log("função declarativa");
}

// funções expressivas
// com nomeacao da funcao
funcaoExpressiva = function expressivaNomeada() {
  console.log("função expressiva com nomeacao");
};

// sem
let funcaoExpressiva = function () {
  console.log("função expressiva");
};

// arrow function
let arrowFunction = () => {
  console.log("arrow function");
};
